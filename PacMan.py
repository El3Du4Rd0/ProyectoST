# dibuja un cuadrado en la posicion x y
def square(x, y):
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for _ in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


# regresa el indice de un punto en el tablero
def offset(point):
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


# verifica si el punto esta dentro del tablero
def valid(point):
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


# dibuja el tablero usando el arreglo de casillas
def world():
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


# mueve a PacMan y a los fantasmas en cada frame del juego
def move():
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    # mueve a los fantasmas
    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(20, 0),
                vector(-20, 0),
                vector(0, 20),
                vector(0, -20),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    # verifica si PacMan colisiona con algun fantasma
    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)


# cambia la direccion de PacMan si la direccion es valida
def change(x, y):
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

# configuracion inicial de la partida
setup(420, 420, 370, 0)
